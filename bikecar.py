import cv2                 
import numpy as np         
import os                  
from random import shuffle 
from tqdm import tqdm     


# In[2]:


TRAIN_DIR ='F:\\bike_car\\train'
TEST_DIR = 'F:\\bike_car\\test1'
IMG_SIZE = 50
LR = 1e-3
print (LR)
MODEL_NAME = 'bikevscar-{}-{}.model'.format(LR, '2conv-basic') 


# In[3]:


def label_img(img):
    word_label = img.split('.')[-3]
    if word_label == 'bik': return [1,0]
    elif word_label == 'car': return [0,1]


# In[4]:


def create_train_data():
    training_data = []
    for img in tqdm(os.listdir(TRAIN_DIR)):
        label = label_img(img)
        path = os.path.join(TRAIN_DIR,img)
        img = cv2.imread(path,cv2.IMREAD_GRAYSCALE)
        img = cv2.resize(img, (IMG_SIZE,IMG_SIZE))
        training_data.append([np.array(img),np.array(label)])
    shuffle(training_data)
    np.save('train_data1.npy', training_data)
    return training_data


# In[5]:


def process_test_data():
    testing_data = []
    for img in tqdm(os.listdir(TEST_DIR)):
        path = os.path.join(TEST_DIR,img)
        img_num = img.split('.')[0]
        img = cv2.imread(path,cv2.IMREAD_GRAYSCALE)
        img = cv2.resize(img, (IMG_SIZE,IMG_SIZE))
        testing_data.append([np.array(img), img_num])
        
    shuffle(testing_data)
    np.save('test_data1.npy', testing_data)
    return testing_data


# In[6]:


train_data = create_train_data()


